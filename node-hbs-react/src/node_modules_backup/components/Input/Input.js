import React, { Component } from 'react';
import PropTypes from 'prop-types';
import classnames from 'classnames';

class Input extends Component {
  static propTypes = {
    errorMessage: PropTypes.string,
    className: PropTypes.string,
    inputClassName: PropTypes.string,
    errorClassName: PropTypes.string,
    errorTextClassName: PropTypes.string,
    inputRef: PropTypes.func
  };

  render() {
    const {
      errorMessage,
      className,
      inputClassName,
      errorClassName,
      errorTextClassName,
      inputRef,
      ...inputAttributes
    } = this.props;
    const inputClasses = classnames('form-control', inputClassName, {
      'has-error': errorMessage
    });
    const errorClasses = classnames('error-message', {
      [errorClassName]: errorMessage && errorClassName
    });
    const errorTextClasses = classnames('error-text', {
      [errorTextClassName]: errorMessage && errorTextClassName
    });

    return (
      <div className={ className }>
        <input
          { ...inputAttributes }
          className={ inputClasses }
          ref={ inputRef }
        />
        { errorMessage && (
          <div className={ errorClasses }>
            <span className="icon icon-form-error error-image"></span>
            <span className={ errorTextClasses }>{ errorMessage }</span>
          </div>
        )}
      </div>
    );
  }
}

export default Input;
