import React from 'react';
import Enzyme, { shallow } from 'enzyme';
import Loader from './Loader';

// TODO: Put this configuration in just one place
import Adapter from 'enzyme-adapter-react-16';
Enzyme.configure({ adapter: new Adapter() });

let component;

describe('Loader component', () => {
  describe('with no theme', () => {
    beforeEach(() => {
      component = shallow(
        <Loader />
      );
    });

    it('should render correctly', () => {
      expect(component).toMatchSnapshot();
    });

    it('should have not set a theme', () => {
      expect(component.props('theme')).toBeNull();
    });
  });

  describe('with theme set to fixed', () => {
    beforeEach(() => {
      component = shallow(
        <Loader theme="fixed" />
      );
    });

    it('should have set a theme class', () => {
      expect(component.find('.spinner--fixed').length).toBe(1);
    });
  });
});
