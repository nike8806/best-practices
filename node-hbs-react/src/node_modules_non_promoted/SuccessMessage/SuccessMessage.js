import React, { PureComponent } from 'react';
import PropTypes from 'prop-types';
import classnames from 'classnames';

/**
 * Document Type component
 *
 * Displays a generic success message
 */
class SuccessMessage extends PureComponent {
  static propTypes = {
    title: PropTypes.string,
    subtitle: PropTypes.string,
    children: PropTypes.node,
    withIcon: PropTypes.bool,
    className: PropTypes.string
  };

  // Setting the default props
  static defaultProps = {
    title: 'Thank you for completing this task.',
    withIcon: false,
    className: ''
  };

  render() {
    const { title, subtitle, withIcon, className, children } = this.props;

    const inputClasses = classnames('success-message__container', 'text-center', className);

    return (
      <div className={inputClasses}>
        <h1 className="success-message__title todo-heading big">
          {title}
        </h1>

        {withIcon && <div className="success-message__success-icon" />}

        {subtitle && (
          <p className="success-message__subtitle todo-text">
            {subtitle}
          </p>
        )}

        {children && (
          <div className="success-message__content">
            {children}
          </div>
        )}

      </div>
    );
  }
}

export default SuccessMessage;
